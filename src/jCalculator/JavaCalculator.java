    private void btnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualActionPerformed
        // This code generates the equal function.
        
        //variable is declared
        int x=txtdisplay.getText().length(); //The innerText is returned by the getText( ) method. These are the codes for retrieving text from txtdisplay. 
        
        fnum= Double.parseDouble(value.substring(0, value.indexOf(" "))); //The indexOf() function returns the location in a string of the first occurrence of the provided character(s).
        snum= Double.parseDouble(value.substring(value.indexOf(" ")+2, x)); //The indexOf() function returns the location in a string of the first occurrence of the provided character(s).
        String operator= value.substring(value.indexOf(" ")+1,value.indexOf(" ")+2); //The indexOf() function returns the location in a string of the first occurrence of the provided character(s).
        
        if(operator.equals("+")) // these codes are for addition
        {   
            ans=fnum + snum;
        }
        if(operator.equals("-")) //these codes are for subtracting
        {   
            ans=fnum - snum;
        }
        if(operator.equals("*")) // these codes are for multiplication
        {   
            ans=fnum * snum;
        }
        if(operator.equals("/")) //these codes are for division
        {   
            ans=fnum / snum;
        }
        
        txtdisplay.setText(value + "=" + String.valueOf(ans)); // The setText() method is used to set the Value string and "=", and the result (complete computation) is displayed in the txtdisplay TextField.
    }//GEN-LAST:event_btnEqualActionPerformed

